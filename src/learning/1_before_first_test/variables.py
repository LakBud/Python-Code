
#(%) : Modulus - The rest after a division
# Example: 5%2 = 1 (since 5/2 = 2 + (answer: 1)/2)

# (//) : Heltallsdivisjon - Number after a division
# Example: 5//2 = 2 (since 5/2 = (answer: 2) + 1/2)

# From Number
a = int(3.1) # a = 3.1 -> 3
b = float(3) # b = 3.0

# From Tekst
a = int("3") # a = 3
b = float("3.3") # b = 3.3
